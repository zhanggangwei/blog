if ( ! function_exists('alert'))
{
    /**
     * [alert 页面跳转]
     * @param  [string] $msg [提示信息]
     * @param  [string] $url [跳转的路径]
     * @return [type]      [description]
     */
    function alert($msg,$url=''){
        echo "<script>alert('$msg');";
        if($url){
            echo "window.location.href='$url';";
        }else{
            echo "window.history.go(-1);";
        }
        echo "</script>";
    }
